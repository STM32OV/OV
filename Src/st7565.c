void st7565_command(uint16_t c)
{
	HAL_GPIO_WritePin(LCD_PORT, LCD_PIN_A0, 0);
	HAL_SPI_Transmit(&SpiHandle, (uint8_t*)&c, 1, 1000);
}

void st7565_data(uint8_t c)
{
  HAL_GPIO_WritePin(LCD_PORT, LCD_PIN_A0, 1);
  HAL_SPI_Transmit(&SpiHandle, (uint8_t*)&c, 1, 1000);
}

void st7565_data_array(uint8_t* pdata, uint8_t size)
{
  HAL_GPIO_WritePin(LCD_PORT, LCD_PIN_A0, 1);
  HAL_SPI_Transmit(&SpiHandle, (uint8_t*)pdata, size, 1000);
}


void _delay_ms()
{
	 HAL_Delay(20);
}

void st7565_set_brightness(uint8_t val)
{
    st7565_command(CMD_SET_VOLUME_FIRST);
    st7565_command(CMD_SET_VOLUME_SECOND | (val & 0x3f));
}


void st7565_init(void)
{
  // set pin directions


  // toggle RST low to reset; CS low so it'll listen to us
  HAL_GPIO_WritePin(LCD_PORT, LCD_PIN_CS, 0);

  // LCD bias select
  st7565_command(CMD_SET_BIAS_7);
  // ADC select
  st7565_command(CMD_SET_ADC_NORMAL);
  // SHL select
  st7565_command(CMD_SET_COM_NORMAL);
  // Initial display line
  st7565_command(CMD_SET_DISP_START_LINE);

  // turn on voltage converter (VC=1, VR=0, VF=0)
  st7565_command(CMD_SET_POWER_CONTROL | 0x4);
  // wait for 50% rising
  _delay_ms(50);

  // turn on voltage regulator (VC=1, VR=1, VF=0)
  st7565_command(CMD_SET_POWER_CONTROL | 0x6);
  // wait >=50ms
  _delay_ms(50);

  // turn on voltage follower (VC=1, VR=1, VF=1)
  st7565_command(CMD_SET_POWER_CONTROL | 0x7);
  // wait
  _delay_ms(10);

  // set lcd operating voltage (regulator resistor, ref voltage resistor)
  st7565_command(CMD_SET_RESISTOR_RATIO | 0x6);

	st7565_command(CMD_DISPLAY_ON);
	st7565_command(CMD_SET_ALLPTS_NORMAL);
	st7565_set_brightness(0);
  // initial display line
  // set page address
  // set column address
  // write display data

  // set up a bounding box for screen updates

//  updateBoundingBox(0, 0, LCDWIDTH-1, LCDHEIGHT-1);
}
